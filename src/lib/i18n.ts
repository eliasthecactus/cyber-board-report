const dictionaries: Record<AppLanguage, Dict> = { en, de };

export function translate(lang: AppLanguage, key: string, vars?: Vars): string {
  let value = dictionaries[lang]?.[key] ?? en[key] ?? key;
  if (vars) {
    for (const [name, replacement] of Object.entries(vars)) {
      value = value.replace(new RegExp(`\\{${name}\\}`, "g"), String(replacement));
    }
  }
  return value;
}

export type TFunction = (key: string, vars?: Vars) => string;

export function useT(): TFunction {
  const { settings } = useSettings();
  const lang = settings.language;
  return (key: string, vars?: Vars) => translate(lang, key, vars);
}

export function useLanguage(): AppLanguage {
  return useSettings().settings.language;
}
